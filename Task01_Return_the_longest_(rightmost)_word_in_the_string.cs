using System;

namespace CSharp_Practice
{
    internal class Program
    {
        // Метод для поиска самого длинного слова
        public static string LongestWord(string stringOfWords)
        {
            string[] words = stringOfWords.Split(' ');
            string theLongest = "";
            foreach (string word in words)
            {
                if (word.Length >= theLongest.Length)
                {
                    theLongest = word;
                }
            }
            return theLongest;
        }

        // Точка входа приложения
        static void Main(string[] args)
        {
            string input = "one two three";
            string result = LongestWord(input);
            Console.WriteLine(result); // Выведет: "three"
        }
    }
}




